package engine.combate.peleitas;

public class SumadorCombate {

    private int sumadorPlayer = 0;

    private int sumadorEnemigo = 0;

    public int getSumadorPlayer() {
        return sumadorPlayer;
    }

    public void setSumadorPlayer(int sumadorPlayer) {
        this.sumadorPlayer = sumadorPlayer;
    }

    public int getSumadorEnemigo() {
        return sumadorEnemigo;
    }

    public void setSumadorEnemigo(int sumadorEnemigo) {
        this.sumadorEnemigo = sumadorEnemigo;
    }
}
